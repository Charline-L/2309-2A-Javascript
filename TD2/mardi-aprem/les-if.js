const age = 19;
// >= : supérieur ou égale
// if = SI
// else = SINON (pour tous les autres cas)
if (age >= 23) {
  console.log("Tu es majeur en Suisse");
} else if (age >= 21) {
  console.log("Tu es majeur aux USA");
} else if (age >= 18) {
  console.log("Tu es majeur en Europe");
} else {
  console.log("Tu es mineur");
}
